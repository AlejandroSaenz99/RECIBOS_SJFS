    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
     
        
          
        if (txtEnvoice.getText().isEmpty() || txtFecha.getDate() == null || txtTotal.getText().isEmpty()|| txtDetails.getText().isEmpty()|| lbImagen.getIcon() == null) {
            JOptionPane.showMessageDialog(null, "Please fill all fields", "empty fields", 1);
        } else {
            DRecibos prod = new DRecibos();
            prod.setEnvoice(txtEnvoice.getText());
            java.util.Date fecha = txtFecha.getDate();
            long d = fecha.getTime();
            java.sql.Date soloFecha = new java.sql.Date(d);
            prod.setDate(soloFecha);
            prod.setTruck(txtCamion.getSelectedItem().toString());
            prod.setTotal(Float.parseFloat(txtTotal.getText()));
            prod.setDetails(txtDetails.getText());
            prod.setYear(Integer.parseInt(a単o.getSelectedItem().toString()));
             prod.setImage(nombreImagen);
         puente(prod);
         txtEnvoice.setText("");
         txtFecha.setDate(null);
         txtTotal.setText("");
         txtDetails.setText("");
        }
               // TODO add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed

